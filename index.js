function calculator(num1, num2, operator){
	return operator(num1, num2)
}

function add (num1, num2){
	return num1 + num2;
}

function subtract (num1, num2){
	return num1 - num2;
}

function multi (num1, num2){
	return 	num1 * num2;
}

function divis (num1, num2){
	return num1 / num2;
}
